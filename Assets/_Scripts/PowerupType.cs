/*
    This script defines an enumeration called PowerupType, which lists the different types of powerups available in the game. 
    Each powerup type corresponds to a specific effect that can be applied to the player when they pick up the powerup.
*/

using UnityEngine;

public enum PowerupType {
    SpreadShot,
    BurstShot,
    FastShoot,
    HomingShot,
    SpeedBoost,
    ReverseShot,
    Shield
}